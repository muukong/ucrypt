static int equal(uint8_t *lhs, uint8_t *rhs, uint32_t length)
{
    int i;
    for ( i = 0; i < length; ++i )
        if ( lhs[i] != rhs[i] )
            return 0;

    return 1;
}

char *test_pbkdf2()
{
    int i;
    uint32_t dk_len;
    uint8_t *derived_key;

    puts("Running PBKDF2 testcases...");

    for ( i = 0; i < NOF_TESTCASES; ++i )
    {
        printf("    Testcase %d\n", i);
        dk_len = pbkdf2_testcases[i].derived_key_length;
        derived_key = malloc(dk_len);

        /* PBKDF-SHA224 */

        uc_pbkdf2_with_prf(UC_PBKDF2_HMAC_SHA224, pbkdf2_testcases[i].secret, pbkdf2_testcases[i].secret_length,
                 pbkdf2_testcases[i].salt, pbkdf2_testcases[i].salt_length,
                 pbkdf2_testcases[i].iter_count, dk_len, derived_key);

        mu_assert("Testcase for PBKDF2-SHA224 failed",
                  equal(derived_key, pbkdf2_testcases[i].sha224_derived_key, dk_len));

        /* PBKDF-SHA256 */

        uc_pbkdf2_with_prf(UC_PBKDF2_HMAC_SHA256, pbkdf2_testcases[i].secret, pbkdf2_testcases[i].secret_length,
                 pbkdf2_testcases[i].salt, pbkdf2_testcases[i].salt_length,
                 pbkdf2_testcases[i].iter_count, dk_len, derived_key);

        mu_assert("Testcase for PBKDF2-SHA256 failed",
                  equal(derived_key, pbkdf2_testcases[i].sha256_derived_key, dk_len));

        /* PBKDF-SHA384 */

        uc_pbkdf2_with_prf(UC_PBKDF2_HMAC_SHA384, pbkdf2_testcases[i].secret, pbkdf2_testcases[i].secret_length,
                 pbkdf2_testcases[i].salt, pbkdf2_testcases[i].salt_length,
                 pbkdf2_testcases[i].iter_count, dk_len, derived_key);

        mu_assert("Testcase for PBKDF2-SHA384 failed",
                  equal(derived_key, (uint8_t *) pbkdf2_testcases[i].sha384_derived_key, dk_len));

        /* PBKDF-SHA512 */
        uc_pbkdf2_with_prf(UC_PBKDF2_HMAC_SHA512, pbkdf2_testcases[i].secret, pbkdf2_testcases[i].secret_length,
                 pbkdf2_testcases[i].salt, pbkdf2_testcases[i].salt_length,
                 pbkdf2_testcases[i].iter_count, dk_len, derived_key);

        mu_assert("Testcase for PBKDF2-SHA512 failed",
                  equal(derived_key, pbkdf2_testcases[i].sha512_derived_key, dk_len));

        free(derived_key);
    }

    return NULL;
}
